public class WeightCon {
    public  static double kg2lbs(double kg){return kg*2.2046;}
    public static double lbs2kg(double lbs){return lbs/2.2046;}

    public static double kg2g(double kg){return kg*1000;}
    public static double g2kg(double g){return g/1000;}

    public static double lbs2g(double lbs){return (lbs/2.2046)*1000;}
    public static double g2lbs(double g){return (g/1000)*2.2046;}


}
