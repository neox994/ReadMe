public class LenghtCon {
    public  static double cm2inch(double cm){return cm*0.39370;}
    public static double inch2cm(double inch){return inch/0.39370;}

    public  static double cm2m(double cm){return cm/1000;}
    public static double m2cm(double m){return m*1000;}

    public  static double m2inch(double m){return (m/1000)*0.39370;}
    public static double inch2m(double inch){return (inch/0.39370)*1000;}


}
